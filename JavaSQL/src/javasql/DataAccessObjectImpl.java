public class DataAccessObjectImpl {
    private int parrentId;
    private int id;
    private String name;

    public DataAccessObjectImpl(int id, String name, int parrentId){
        this.setId(id);
        this.setName(name);
        this.setParrentId(parrentId);
    }

    public int getParrentId() {
        return parrentId;
    }

    public void setParrentId(int parrentId) {
        this.parrentId = parrentId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
